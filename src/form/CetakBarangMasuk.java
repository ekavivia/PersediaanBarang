    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            File file = new File("src/laporan/barangMasuk.jrxml");
            jasDes = JRXmlLoader.load(file);
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            String tglAwal = format.format(new java.sql.Date(dcAwal.getDate().getTime()));
            String tglAkhir = format.format(new java.sql.Date(cdAkhir.getDate().getTime()));
            param.put("awal", tglAwal);
            param.put("akhir", tglAkhir);
            jasRep = JasperCompileManager.compileReport(jasDes);
            jasPri = JasperFillManager.fillReport(jasRep,param,new fungsi.koneksi().konek);
            JasperViewer.viewReport(jasPri,false);
            setVisible(false);
        } catch (Exception e) {
            System.out.println(""+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

