    public void kodeOtomatis(){
        try {
            String sql = "select * from barang_masuk order by kode_brg_masuk desc";
            stat = new fungsi.koneksi().konek.createStatement();
            res = stat.executeQuery(sql);
            if (res.next()) {
                String kode = res.getString("kode_brg_masuk").substring(1);
                String angka = "" + (Integer.parseInt(kode) + 1);
                String nol = "";

                if(angka.length()==1){
                    nol = "00";
                }else if(angka.length()==2){
                    nol = "0";
                }else if(angka.length()==3){
                    nol = "";
                }
                tfKodeBrgMasuk.setText("P" + nol + angka);
            }else {
               tfKodeBrgMasuk.setText("P001");
            }
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
        }
    }

