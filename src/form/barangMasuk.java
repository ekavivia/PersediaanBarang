    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{
            int baris = tabModel.getRowCount();
            for(int a=0; a<baris; a++){
                String sql1 = "insert into barang_masuk (kode_brg_masuk,kode_barang,tanggal,jumlah) values (?,?,?,?)";
                prestat = new fungsi.koneksi().konek.prepareStatement(sql1);
                prestat.setString(1, tfKodeBrgMasuk.getText());
                prestat.setString(2, tbDetail.getValueAt(a, 0).toString());
                prestat.setDate(3, new java.sql.Date(dcTanggal.getDate().getTime()));
                prestat.setInt(4, Integer.parseInt(tbDetail.getValueAt(a, 3).toString()));
                prestat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data telah disimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(Exception e){
            System.out.println(""+e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

